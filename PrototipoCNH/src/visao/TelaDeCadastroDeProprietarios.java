    private void mostrarDados() {
        try {
            ArrayList<Proprietario> listaDeProprietarios = null;
            Telefone fone = null;
            listaDeProprietarios = proprietarioControle.ObterListagemDeContatos();
            DefaultTableModel model = (DefaultTableModel) jTableSaida.getModel();
            model.setNumRows(0);
            if (listaDeProprietarios.isEmpty()) {
                return;
            }
            for (int pos = 0; pos < listaDeProprietarios.size(); pos++) {
                Proprietario listaProprietarios = listaDeProprietarios.get(pos);

                fone = listaProprietarios.getFone();

                String[] linha = new String[7];

                linha[0] = listaProprietarios.getCpf();
                linha[1] = listaProprietarios.getNomecompleto();
                linha[2] = listaProprietarios.getEmail();
                linha[3] = "+" + fone.getDdi() + fone.getDdd() + fone.getNumero();
                linha[4] = listaProprietarios.getCnh();
                linha[5] = listaProprietarios.getCategoria();
                linha[6] = listaProprietarios.getImagem();

                //linha     
                model.addRow(linha);
            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(rootPane, erro.getMessage());
        }
    }
