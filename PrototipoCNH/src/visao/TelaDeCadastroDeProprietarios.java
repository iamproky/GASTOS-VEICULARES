    private void jButtonAlterarProprietarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarProprietarioActionPerformed
        try {

           String cpf = jFormattedTextFieldCpf.getText();
            String nomecompleto = jTextFieldNomeCompleto.getText();
            String email = jTextFieldEmail.getText();

            Telefone fone = new Telefone((jFormattedTextFieldDDI.getText()),
                                         (jFormattedTextFieldDDD.getText()),
                                         (jFormattedTextFieldNumero.getText()));

            String cnh = jFormattedTextFieldCNH.getText();
            String categoria = (String) jComboBoxCategoria.getSelectedItem();

            Proprietario proprietario = new Proprietario(cpf, nomecompleto, email, fone, cnh, categoria, caminho);

            proprietarioControle.Alterar(proprietario);
            
            
            JOptionPane.showMessageDialog(this, "ALTERADO COM SUCESSO");
            mostrarDados();
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro.getMessage());
            
        }
    }//GEN-LAST:event_jButtonAlterarProprietarioActionPerformed

