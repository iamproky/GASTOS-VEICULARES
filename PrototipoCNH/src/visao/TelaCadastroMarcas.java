    private void jTableSaidaMarcaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableSaidaMarcaMouseClicked
        
        try {
            int numeroLinha = jTableSaidaMarca.getSelectedRow();
            
            ArrayList<Marca> ObterListagemDeMarcas = null;
            ObterListagemDeMarcas = marcaControle.ObterListagemDeMarcas();
            Marca i = ObterListagemDeMarcas.get(numeroLinha);

            jTextFieldIdDaMarca.setText(""+i.getId());
            //jComboBoxMarcaVeiculo.set(""+i.getMarca());
            
            
            logo = ""+i.getLogo();
            
            BufferedImage bi = ImageIO.read(new File(logo) );
            Image img = bi.getScaledInstance(131, 166,Image.SCALE_SMOOTH);
            ImageIcon icon = new ImageIcon(img);
            jLabelLogoDaMarca.setIcon(icon);

        } catch (Exception erro) {

            JOptionPane.showMessageDialog(null, erro.getMessage());
        }

    }//GEN-LAST:event_jTableSaidaMarcaMouseClicked

    private void jButtonSelecionarLogoMarcaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSelecionarLogoMarcaActionPerformed
        try {
            JFileChooser chooser = new JFileChooser();
            chooser.showOpenDialog(null);
            File f = chooser.getSelectedFile();
            logo = f.getAbsolutePath();
            BufferedImage bi = ImageIO.read(new File(logo) );
            Image img = bi.getScaledInstance(131, 166,Image.SCALE_SMOOTH);
            ImageIcon icon = new ImageIcon(img);
            jLabelLogoDaMarca.setIcon(icon);
            
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, erro.getMessage());
        }

            
        
        
        
    }//GEN-LAST:event_jButtonSelecionarLogoMarcaActionPerformed

