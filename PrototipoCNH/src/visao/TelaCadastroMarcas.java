    private void mostrarDados(){
    try {
      ArrayList<Marca> listaDeMarcas = null;
      listaDeMarcas = marcaControle.ObterListagemDeMarcas();
      DefaultTableModel model =  (DefaultTableModel) jTableSaidaMarca.getModel();
      model.setNumRows(0);
      if(listaDeMarcas.isEmpty()) return;
      for(int pos = 0; pos < listaDeMarcas.size(); pos++){
      Marca listaMarcas = listaDeMarcas.get(pos);
         
     
        
        String[] linha = new String[3];
        
        linha[0]= listaMarcas.getId();
        linha[1]= listaMarcas.getMarca();
        linha[2] = listaMarcas.getLogo();        
          
        
        
        //linha     
        model.addRow(linha);
      }
    } catch (Exception erro) {
      JOptionPane.showMessageDialog(rootPane, erro.getMessage());
    }
  }

